var countCharacters = function(words, chars) {
    var cnt = 0;
    words.forEach(word => {
      var char = chars;
      var len = 0;
      for(var i = 0;i<word.length;i++){
        if(char.indexOf(word[i])!=-1){
          len +=1;
          char = char.replace(new RegExp(word[i]),'+')  //将char表中有word[i]的字母换成其他，例如加号，防止出现下一次有又匹配到这个字母
        }else{
          break
        }
      }
      if(len==word.length){
        cnt+=word.length
      }
    });
    return cnt
  };
